# Create a tuple
my_tuple = ('banana', 'mango', 'orange')

# Access tuple items
print(my_tuple)

# Change tuple values
tuple_list = list(my_tuple)
tuple_list[0] = 'cherry'

# Loop through a tuple
for x in my_tuple:
    print(x)

# Check if a tuple item exists
# Get the length of a tuple
print(len(my_tuple))

# Delete a tuple
del my_tuple

